package collection;

public class Maestria {
	
	private boolean bauCamp;
    private long maestriaCampLvl;
    private long maestriaCampPt;
    private long maestriaCampId;
    private String maestriaCampNome;

    public boolean isBauCamp() {
        return bauCamp;
    }

    public void setBauCamp(boolean bauCamp) {
        this.bauCamp = bauCamp;
    }

    public long getMaestriaCampLvl() {
        return maestriaCampLvl;
    }

    public void setMaestriaCampLvl(long maestriaCampLvl) {
        this.maestriaCampLvl = maestriaCampLvl;
    }

    public long getMaestriaCampPt() {
        return maestriaCampPt;
    }

    public void setMaestriaCampPt(long maestriaCampPt) {
        this.maestriaCampPt = maestriaCampPt;
    }

    public long getMaestriaCampId() {
        return maestriaCampId;
    }

    public void setMaestriaCampId(long maestriaCampId) {
        this.maestriaCampId = maestriaCampId;
    }

    public String getMaestriaCampNome() {
        return maestriaCampNome;
    }

    public void setMaestriaCampNome(String maestriaCampNome) {
        this.maestriaCampNome = maestriaCampNome;
    }
}
